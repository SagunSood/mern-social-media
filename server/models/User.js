import mongoose from "mongoose";

const UserSchema = new mongoose.Schema(
  {
    firstName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    lastName: {
      type: String,
      required: true,
      min: 2,
      max: 50,
    },
    email: {
      type: String,
      required: true,
      max: 50,
      unique: true,
    },
    password: {
      type: String,
      required: true,
      min: 5,
    },
    picturePath: {
      type: String,
      default: "",
    },
    friends: {
      type: Array,
      default: [],
    },
    location: String,
    occupation: String,
    viewedProfile: Number,
    impressions: Number,
  },
  { timestamps: true }
);

// Add the findOrCreate method to the User model
UserSchema.statics.findOrCreate = async function (conditions, doc) {
  const user = await this.findOne(conditions);
  if (user) {
    return user;
  } else {
    return this.create(doc);
  }
};

const User = mongoose.model("User", UserSchema);
export default User;
